package ex1;

public class Calcul {

    /**
     * @param a: opérande entier
     * @param b: opérande entier
     * @return int
     */
    public int addition(int a, int b) {
        return a + b;
    }

    /**
     * @param a: opérande entier
     * @param b: opérande entier
     * @return int
     */
    public int soustraction(int a, int b) {
        return a - b;
    }
}
